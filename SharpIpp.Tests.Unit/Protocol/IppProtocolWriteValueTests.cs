using FluentAssertions;
using Microsoft.VisualStudio.TestTools.UnitTesting;
using SharpIpp.Protocol;
using SharpIpp.Protocol.Models;
using System.IO;
using System.Text;

namespace SharpIpp.Tests.Unit.Protocol;

[TestClass]
public class IppProtocolWriteValueTests
{
    private byte[] GetBytes(object value)
    {
        var protocol = new IppProtocol();
        using MemoryStream memoryStream = new();
        using BinaryWriter binaryWriter = new(memoryStream, Encoding.ASCII, true);
        protocol.WriteValue(value, binaryWriter, Encoding.ASCII);
        return memoryStream.ToArray();
    }

    [TestMethod]
    [DataRow(Tag.Integer, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x21 })]
    [DataRow(IppStatusCode.SuccessfulOk, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x00 })]
    [DataRow(IppOperation.PrintJob, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x02 })]
    [DataRow(JobState.Pending, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x03 })]
    [DataRow(DocumentState.Pending, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x03 })]
    [DataRow(PrinterState.Idle, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x03 })]
    [DataRow(PrintQuality.Normal, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x04 })]
    [DataRow(ResolutionUnit.DotsPerInch, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x03 })]
    [DataRow(PrinterType.PrinterClass, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x01 })]
    [DataRow(Orientation.Portrait, new byte[] { 0x00, 0x04, 0x00, 0x00, 0x00, 0x03 })]
    public void WriteValue_IntegerBasedEnums_ShouldBeWrittenAsInt32(object value, byte[] expected)
    {
        GetBytes(value).Should().Equal(expected);
    }

    [TestMethod]
    [DataRow(JobSheets.None, new byte[] { 0x00, 0x04, 0x6E, 0x6F, 0x6E, 0x65 })]
    [DataRow(MultipleDocumentHandling.SeparateDocumentsUncollatedCopies, new byte[] { 0x00, 0x24, 0x73, 0x65, 0x70, 0x61, 0x72, 0x61, 0x74, 0x65, 0x2D, 0x64, 0x6F, 0x63, 0x75, 0x6D, 0x65, 0x6E, 0x74, 0x73, 0x2D, 0x75, 0x6E, 0x63, 0x6F, 0x6C, 0x6C, 0x61, 0x74, 0x65, 0x64, 0x2D, 0x63, 0x6F, 0x70, 0x69, 0x65, 0x73 })]
    [DataRow(Sides.OneSided, new byte[] { 0x00, 0x09, 0x6F, 0x6E, 0x65, 0x2D, 0x73, 0x69, 0x64, 0x65, 0x64 })]
    [DataRow(PrintScaling.Auto, new byte[] { 0x00, 0x04, 0x61, 0x75, 0x74, 0x6F })]
    [DataRow(Compression.None, new byte[] { 0x00, 0x04, 0x6E, 0x6F, 0x6E, 0x65 })]
    [DataRow(UriScheme.Ipp, new byte[] { 0x00, 0x03, 0x69, 0x70, 0x70 })]
    [DataRow(UriAuthentication.None, new byte[] { 0x00, 0x04, 0x6E, 0x6F, 0x6E, 0x65 })]
    [DataRow(UriSecurity.None, new byte[] { 0x00, 0x04, 0x6E, 0x6F, 0x6E, 0x65 })]
    [DataRow(PrintColorMode.Color, new byte[] { 0x00, 0x05, 0x63, 0x6F, 0x6C, 0x6F, 0x72 })]
    [DataRow(WhichJobs.Completed, new byte[] { 0x00, 0x09, 0x63, 0x6F, 0x6D, 0x70, 0x6C, 0x65, 0x74, 0x65, 0x64 })]
    public void WriteValue_KebabCaseEnums_ShouldBeWrittenAsKebabCaseStrings(object value, byte[] expected)
    {
        GetBytes(value).Should().Equal(expected);
    }

    [TestMethod]
    [DataRow(BalingWhen.AfterJob, new byte[] { 0x00, 0x09, 0x61, 0x66, 0x74, 0x65, 0x72, 0x2D, 0x6A, 0x6F, 0x62 })]
    [DataRow(FinishingReferenceEdge.Left, new byte[] { 0x00, 0x04, 0x6C, 0x65, 0x66, 0x74 })]
    [DataRow(CoatingSides.Both, new byte[] { 0x00, 0x04, 0x62, 0x6F, 0x74, 0x68 })]
    [DataRow(FoldingDirection.Inward, new byte[] { 0x00, 0x06, 0x69, 0x6E, 0x77, 0x61, 0x72, 0x64 })]
    [DataRow(StitchingMethod.Wire, new byte[] { 0x00, 0x04, 0x77, 0x69, 0x72, 0x65 })]
    [DataRow(TrimmingType.DrawLine, new byte[] { 0x00, 0x09, 0x64, 0x72, 0x61, 0x77, 0x2D, 0x6C, 0x69, 0x6E, 0x65 })]
    [DataRow(TrimmingWhen.AfterJob, new byte[] { 0x00, 0x09, 0x61, 0x66, 0x74, 0x65, 0x72, 0x2D, 0x6A, 0x6F, 0x62 })]
    [DataRow(CoverType.PrintBoth, new byte[] { 0x00, 0x0A, 0x70, 0x72, 0x69, 0x6E, 0x74, 0x2D, 0x62, 0x6F, 0x74, 0x68 })]
    [DataRow(PresentationDirectionNumberUp.TobottomToleft, new byte[] { 0x00, 0x0F, 0x74, 0x6F, 0x62, 0x6F, 0x74, 0x74, 0x6F, 0x6D, 0x2D, 0x74, 0x6F, 0x6C, 0x65, 0x66, 0x74 })]
    [DataRow(XImagePosition.Center, new byte[] { 0x00, 0x06, 0x63, 0x65, 0x6E, 0x74, 0x65, 0x72 })]
    [DataRow(YImagePosition.Center, new byte[] { 0x00, 0x06, 0x63, 0x65, 0x6E, 0x74, 0x65, 0x72 })]
    [DataRow(PdlOverride.Attempted, new byte[] { 0x00, 0x09, 0x61, 0x74, 0x74, 0x65, 0x6D, 0x70, 0x74, 0x65, 0x64 })]
    [DataRow(BalingType.Band, new byte[] { 0x00, 0x04, 0x62, 0x61, 0x6E, 0x64 })]
    [DataRow(BindingType.Perfect, new byte[] { 0x00, 0x07, 0x70, 0x65, 0x72, 0x66, 0x65, 0x63, 0x74 })]
    [DataRow(CoatingType.Glossy, new byte[] { 0x00, 0x06, 0x67, 0x6C, 0x6F, 0x73, 0x73, 0x79 })]
    [DataRow(CoveringName.Plain, new byte[] { 0x00, 0x05, 0x70, 0x6C, 0x61, 0x69, 0x6E })]
    [DataRow(LaminatingType.Archival, new byte[] { 0x00, 0x08, 0x61, 0x72, 0x63, 0x68, 0x69, 0x76, 0x61, 0x6C })]
    [DataRow(PrinterStateReason.None, new byte[] { 0x00, 0x04, 0x6E, 0x6F, 0x6E, 0x65 })]
    [DataRow(JobHoldUntil.NoHold, new byte[] { 0x00, 0x07, 0x6E, 0x6F, 0x2D, 0x68, 0x6F, 0x6C, 0x64 })]
    [DataRow(JobSpooling.Spool, new byte[] { 0x00, 0x05, 0x73, 0x70, 0x6F, 0x6F, 0x6C })]
    [DataRow(PrintContentOptimize.Text, new byte[] { 0x00, 0x04, 0x74, 0x65, 0x78, 0x74 })]
    [DataRow(JobSheetsType.Standard, new byte[] { 0x00, 0x08, 0x73, 0x74, 0x61, 0x6E, 0x64, 0x61, 0x72, 0x64 })]
    [DataRow(JobErrorSheetWhen.OnError, new byte[] { 0x00, 0x08, 0x6F, 0x6E, 0x2D, 0x65, 0x72, 0x72, 0x6F, 0x72 })]
    [DataRow(SeparatorSheetsType.SlipSheets, new byte[] { 0x00, 0x0B, 0x73, 0x6C, 0x69, 0x70, 0x2D, 0x73, 0x68, 0x65, 0x65, 0x74, 0x73 })]
    public void WriteValue_MoreKebabCaseEnums_ShouldBeWrittenAsKebabCaseStrings(object value, byte[] expected)
    {
        GetBytes(value).Should().Equal(expected);
    }
}
